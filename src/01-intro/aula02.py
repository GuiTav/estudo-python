# Palavras reservadas: Não podem ser usadas para nomear variáveis, constantes etc.
# Exemplos: True, False, None, and, import, lambda.

# Indentificadores são nomes de variáveis, classes ou funções
# Python é case-sensitive
# Nomes de variáveis devem começar com letras ou _
# Sem espaços em branco ou caracteres especiais

nome = "Maria"
idade = 30

# A convenção para nomeação de variáveis é a snake_case

nome_completo = "Maria dos Anjos"

# Constantes devem ser nomeadas em letras maiúsculas

PI = 3.14159265

# Nomear de forma clara, segundo a utilidade da variável

c = 10 + 20 # ERRADO
contador = 10 + 20 # CERTO